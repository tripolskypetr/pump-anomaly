import { ParserItem } from "./types";

/**
 * ПЛАЦЕБО-КОНТРОЛЬ — доказательство, что эдж именно В ПОСТАХ, а не в рынке.
 *
 * DSR/PBO/SPA защищают от перебора конфигов, но НЕ от вопроса «а нужен ли сигнал
 * вообще»: на растущем рынке любой генератор случайных лонгов «зарабатывает».
 * Контроль классический: тот же пайплайн на тех же свечах, но информация о
 * ВРЕМЕНИ постов уничтожена — все ts канала сдвинуты на общий случайный лаг.
 * Если «эдж» плацебо сопоставим с реальным, доход приходит из свойств рынка
 * (дрифт, волатильность), а не из информации в постах.
 *
 * Сдвиг устроен так, чтобы сломать ТОЛЬКО привязку к рынку:
 *  - каждый канал сдвигается на СВОЙ детерминированный лаг (хэш имени) —
 *    межканальные совпадения (matrix-эдж) тоже плацебируются;
 *  - лаг общий для всех постов канала — внутриканальные интервалы сохранены,
 *    algo-слои (решётка интервалов, cron) видят ту же механику постинга;
 *  - сдвиг ТОЛЬКО НАЗАД (3–14 дней) — свечи в прошлом гарантированно существуют,
 *    look-ahead в будущее невозможен;
 *  - всё детерминировано (без Math.random) — плацебо воспроизводимо.
 */

/** диапазон сдвига назад, дней: ≥3 — дальше автокорреляции рынка, ≤14 — тот же режим */
const PLACEBO_SHIFT_MIN_DAYS = 3;
const PLACEBO_SHIFT_SPAN_DAYS = 12; // 3..14 включительно (12 значений + дробная часть)

/** FNV-1a: детерминированный хэш имени канала → [0,1) */
const hash01 = (s: string): number => {
  let h = 0x811c9dc5;
  for (let i = 0; i < s.length; i++) {
    h ^= s.charCodeAt(i);
    h = Math.imul(h, 0x01000193);
  }
  return (h >>> 0) / 0x100000000;
};

/**
 * Плацебо-версия parser-items: ts каждого канала сдвинут назад на его
 * детерминированный лаг (3–14 дней, с точностью до минуты). Остальные поля
 * (symbol, direction, зоны входа) не трогаются.
 */
export function placeboItems(items: ParserItem[]): ParserItem[] {
  const DAY = 86_400_000;
  const MINUTE = 60_000;
  return items.map((it) => {
    if (!it || !Number.isFinite(it.ts)) return it;
    const u = hash01(String(it.channel ?? ""));
    const shiftMs = Math.round(((PLACEBO_SHIFT_MIN_DAYS + u * PLACEBO_SHIFT_SPAN_DAYS) * DAY) / MINUTE) * MINUTE;
    return { ...it, ts: it.ts - shiftMs };
  });
}
